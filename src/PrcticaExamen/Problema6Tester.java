
package PrcticaExamen;


public class Problema6Tester {
    public int AreaCuadrado(int lado){
        int resultado = 0;
        resultado = lado * lado;
        return resultado;
        
    }
    public int AreaRectangulo(int base, int altura){
        int resultado = 0;
        resultado = base*altura;
        return resultado;
    }
    public double AreaCirculo(int radio){
        double Pi = 3.14;
        double resultado = 0;
        resultado = Pi*(radio*radio);
        return resultado;
        
    }
    
}
